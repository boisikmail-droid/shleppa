def main() -> int:
    grouped = assign_words()
    existing: set[str] = set()
    for cat in grouped:
        for lvl in grouped[cat]:
            existing.update(x.lower() for x in grouped[cat][lvl])

    out_lines = ['# -*- coding: utf-8 -*-\n\nNOVEL3: dict[str, dict[int, list[str]]] = {\n']
    total = 0
    for cat, levels in CANDIDATES.items():
        out_lines.append(f'    "{cat}": {{')
        for lvl, text in sorted(levels.items(), key=lambda x: int(x[0])):
            mx = MAX_LEVEL.get(cat, DEFAULT_MAX)
            if int(lvl) > mx:
                continue
            words = []
            for raw in w(text):
                c = clean_word(raw, int(lvl), cat)
                if not c:
                    continue
                k = c.lower()
                if k in existing:
                    continue
                existing.add(k)
                words.append(c)
            if words:
                total += len(words)
                out_lines.append(f"        {lvl}: [")
                for word in words:
                    esc = word.replace("\\", "\\\\").replace("'", "\\'")
                    out_lines.append(f"            '{esc}',")
                out_lines.append("        ],")
        out_lines.append("    },")
    out_lines.append("}\n")
    path = WORDS_DIR / "novel_ru3.py"
    path.write_text("\n".join(out_lines), encoding="utf-8")
    print(f"Wrote {total} novel words to {path}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
