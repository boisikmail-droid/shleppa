from __future__ import annotations

import re
from pathlib import Path

OUT_DIR = Path(__file__).resolve().parent
LATIN_RE = re.compile(r"[a-zA-Z]")


def w(text: str) -> list[str]:
    return [x.strip() for x in text.replace("\n", " ").split(",") if x.strip()]


def dedupe(words: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for word in words:
        key = word.casefold()
        if key in seen:
            continue
        seen.add(key)
        out.append(word)
    return out


def clean(words: list[str], level: int) -> list[str]:
    out: list[str] = []
    for word in words:
        if not word or " " in word:
            continue
        if level <= 5 and LATIN_RE.search(word):
            continue
        out.append(word)
    return out


def dedupe_global(levels: dict[int, list[str]]) -> dict[int, list[str]]:
    seen: set[str] = set()
    out: dict[int, list[str]] = {lvl: [] for lvl in levels}
    for lvl in sorted(levels):
        for word in dedupe(levels[lvl]):
            key = word.casefold()
            if key in seen:
                continue
            seen.add(key)
            out[lvl].append(word)
    return out


def write_py(path: Path, levels: dict[int, list[str]]) -> dict[int, int]:
    counts: dict[int, int] = {}
    lines = ["LEVELS = {"]
    for lvl in sorted(levels):
        items = dedupe(levels[lvl])
        counts[lvl] = len(items)
        lines.append(f"    {lvl}: [")
        for word in items:
            esc = word.replace("\\", "\\\\").replace('"', '\\"')
            lines.append(f'        "{esc}",')
        lines.append("    ],")
    lines.append("}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")
    return counts


