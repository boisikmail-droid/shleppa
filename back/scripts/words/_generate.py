def parse_block(text: str) -> list[str]:
    result: list[str] = []
    seen: set[str] = set()
    for raw in text.replace("\n", ",").split(","):
        item = " ".join(raw.strip().split())
        if item and item.casefold() not in seen:
            result.append(item)
            seen.add(item.casefold())
    return result


def _u(*chunks: str) -> list[str]:
    return parse_block(",".join(chunks))


def quote_chunk(words: list[str]) -> str:
    return ", ".join(w.replace("\\", "\\\\").replace('"', '\\"') for w in words)


def validate_levels(name: str, levels: dict[int, list[str]]) -> None:
    expected_levels = {
        "movies": {1, 2, 3, 4, 5},
        "sport": {1, 2, 3, 4},
        "feelings": {1, 2, 3, 4, 5},
    }[name]
    if set(levels) != expected_levels:
        raise ValueError(f"{name}: expected levels {sorted(expected_levels)}, got {sorted(levels)}")

    total = 0
    for level, words in sorted(levels.items()):
        total += len(words)
        seen: set[str] = set()
        for word in words:
            key = word.casefold()
            if key in seen:
                raise ValueError(f"{name} L{level}: duplicate {word!r}")
            seen.add(key)
            if not word:
                raise ValueError(f"{name} L{level}: empty word")
            if name in {"movies", "sport", "feelings"} and level <= 4 and LATIN.search(word):
                raise ValueError(f"{name} L{level}: Latin letters in {word!r}")
            if word in BANNED_EXACT:
                raise ValueError(f"{name} L{level}: banned item {word!r}")
            low = word.casefold()
            if any(part.casefold() in low for part in BANNED_SUBSTRINGS):
                raise ValueError(f"{name} L{level}: padded-looking item {word!r}")
            if name == "feelings":
                if SPACE.search(word):
                    raise ValueError(f"feelings L{level}: not a single word {word!r}")
                if word.casefold() in FEELINGS_DIAGNOSES:
                    raise ValueError(f"feelings L{level}: clinical diagnosis {word!r}")
    if total < MIN_TOTALS[name]:
        raise ValueError(f"{name}: total {total}, need {MIN_TOTALS[name]}+")


def emit(name: str, doc: str, levels: dict[int, list[str]]) -> dict[int, int]:
    validate_levels(name, levels)
    lines = [
        "# -*- coding: utf-8 -*-",
        f'"""{doc}"""',
        "from __future__ import annotations",
        "",
        "",
        "def _u(*chunks: str) -> list[str]:",
        "    result: list[str] = []",
        "    seen: set[str] = set()",
        "    for chunk in chunks:",
        '        for item in chunk.split(","):',
        '            item = " ".join(item.strip().split())',
        "            if item and item.casefold() not in seen:",
        "                result.append(item)",
        "                seen.add(item.casefold())",
        "    return result",
        "",
        "",
        "LEVELS: dict[int, list[str]] = {",
    ]
    counts: dict[int, int] = {}
    for level in sorted(levels):
        words = levels[level]
        counts[level] = len(words)
        lines.append(f"    {level}: _u(")
        for index in range(0, len(words), 9):
            lines.append(f'        "{quote_chunk(words[index:index + 9])}",')
        lines.append("    ),")
    lines.append("}")
    lines.append("")
    (OUT / f"{name}.py").write_text("\n".join(lines), encoding="utf-8")
    return counts


