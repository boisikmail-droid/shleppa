"""Large Cyrillic word pools — validated single nouns only."""
from __future__ import annotations

import re

LATIN = re.compile(r"[a-zA-Z]")
CYR = re.compile(r"^[а-яА-ЯёЁ]+$")


def p(text: str) -> list[str]:
    return [w.strip() for w in text.split() if w.strip()]


def v(words: list[str], level: int) -> list[str]:
    out: list[str] = []
    seen: set[str] = set()
    for w in words:
        if not w or " " in w or "-" in w:
            continue
        if level <= 5 and (LATIN.search(w) or not CYR.match(w)):
            continue
        k = w.casefold()
        if k in seen:
            continue
        seen.add(k)
        out.append(w)
    return out


