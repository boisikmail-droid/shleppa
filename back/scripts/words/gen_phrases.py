def is_junk_token(tok: str) -> bool:
    """Отсекает уменьшительный и словарный мусор вроде помидорик / смородинаище."""
    low = tok.casefold()
    if re.search(r"(ище|озик|евозик|ичек|ничек|ик|чик|ушка|юшка|очка|ечка)$", low) and len(low) >= 7:
        # исключения — обычные слова
        if low in {
            "холодильник", "выключатель", "подоконник", "микроволновка", "соковыжималка",
            "библиотека", "поликлиника", "химчистка", "спецификация", "презентация",
            "квалификация", "организация", "регистратура", "инфраструктура",
            "бантик", "зонтик", "винтик", "мостик", "столик", "коврик", "пряник",
        }:
            return False
        if low.endswith(("ище", "озик", "евозик", "ичек", "ничек")):
            # реальные -ище пропускаем
            if low.endswith("ище") and low in {
                "кладбище", "пастбище", "училище", "голенище", "жилище", "убежище",
                "водохранилище", "книгохранилище", "зернохранилище", "профтехучилище",
            }:
                return False
            return True
        if low.endswith(("ик", "чик")) and len(low) >= 9:
            return True
    return False


def phrase_quality_ok(phrase: str) -> bool:
    parts = phrase.split()
    if len(parts) < 3 or len(parts) > 4:
        return False
    if any(re.search(r"[A-Za-z]", p) for p in parts):
        return False
    if any(is_junk_token(p) for p in parts if p.casefold() not in STOPWORDS):
        return False
    return True


