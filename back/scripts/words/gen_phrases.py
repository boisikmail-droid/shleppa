def build_random_themes() -> dict[int, tuple]:
    slices = load_lexicon_slices()
    themes: dict[int, tuple] = {}
    for lvl in range(1, 6):
        base_adj, base_noun, base_verb, base_adv = RANDOM_THEMES_BASE[lvl]
        lex_adj, lex_noun = slices.get(lvl, ([], []))
        adj = list(dict.fromkeys(base_adj + lex_adj))
        noun = list(dict.fromkeys(base_noun + lex_noun))
        verb = list(dict.fromkeys(base_verb + EXTRA_VERBS.get(lvl, [])))
        adv = list(dict.fromkeys(base_adv + EXTRA_ADV.get(lvl, [])))
        # фильтр: однословные
        adj = [w for w in adj if " " not in w]
        noun = [w for w in noun if " " not in w]
        verb = [w for w in verb if " " not in w]
        adv = [w for w in adv if " " not in w]
        themes[lvl] = (adj, noun, verb, adv)
        print(f"  theme L{lvl}: adj={len(adj)} noun={len(noun)} verb={len(verb)} adv={len(adv)}")
    return themes


