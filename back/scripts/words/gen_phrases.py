def split_csv(text: str) -> list[str]:
    out: list[str] = []
    seen: set[str] = set()
    for part in text.replace("\n", ",").split(","):
        p = part.strip()
        if not p or " " in p:
            continue
        k = p.casefold()
        if k in seen:
            continue
        seen.add(k)
        out.append(p)
    return out


def parse_phrases(text: str) -> list[str]:
    out: list[str] = []
    seen: set[str] = set()
    for line in text.strip().splitlines():
        if not line.strip() or line.strip().startswith("#"):
            continue
        for part in line.split(","):
            p = " ".join(part.split())
            if not p:
                continue
            n = len(p.split())
            if n < 3 or n > 4:
                continue
            k = p.casefold()
            if k in seen:
                continue
            seen.add(k)
            out.append(p)
    return out


def content_tokens(phrase: str) -> list[str]:
    return [t for t in phrase.casefold().split() if t not in STOPWORDS]


def phrase_ok(phrase: str, counts: Counter[str], max_occ: int = MAX_OCC) -> bool:
    words = phrase.split()
    if len(words) < 3 or len(words) > 4:
        return False
    toks = content_tokens(phrase)
    if len(toks) < 2:
        return False
    local: Counter[str] = Counter()
    for tok in toks:
        if counts[tok] + local[tok] + 1 > max_occ:
            return False
        local[tok] += 1
    return True


def commit(phrase: str, counts: Counter[str]) -> None:
    for tok in content_tokens(phrase):
        counts[tok] += 1


def available(words: list[str], counts: Counter[str], max_occ: int = MAX_OCC) -> list[str]:
    return [w for w in words if counts[w.casefold()] < max_occ]


def build_from_slots(
    adj: list[str],
    noun: list[str],
    verb: list[str],
    adv: list[str],
    need: int,
    counts: Counter[str],
    used: set[str],
) -> list[str]:
    """Жадный набор фраз из слов с оставшимся бюджетом."""
    picked: list[str] = []
    attempts = 0
    max_attempts = need * 80

    while len(picked) < need and attempts < max_attempts:
        attempts += 1
        a_pool = available(adj, counts)
        n_pool = available(noun, counts)
        v_pool = available(verb, counts)
        d_pool = available(adv, counts)

        phrase = None
        mode = attempts % 5
        if mode == 0 and a_pool and n_pool and v_pool and d_pool:
            phrase = f"{RNG.choice(a_pool)} {RNG.choice(n_pool)} {RNG.choice(v_pool)} {RNG.choice(d_pool)}"
        elif mode == 1 and a_pool and n_pool and v_pool:
            phrase = f"{RNG.choice(a_pool)} {RNG.choice(n_pool)} {RNG.choice(v_pool)}"
        elif mode == 2 and n_pool and v_pool and d_pool:
            phrase = f"{RNG.choice(n_pool)} {RNG.choice(v_pool)} {RNG.choice(d_pool)}"
        elif mode == 3 and a_pool and n_pool and d_pool:
            # без глагола, когда глаголы исчерпаны
            phrase = f"{RNG.choice(a_pool)} {RNG.choice(n_pool)} {RNG.choice(d_pool)}"
        elif len(n_pool) >= 2 and a_pool:
            n1, n2 = RNG.sample(n_pool, 2)
            phrase = f"{RNG.choice(a_pool)} {n1} {n2}"
        elif len(n_pool) >= 2 and v_pool:
            n1, n2 = RNG.sample(n_pool, 2)
            phrase = f"{n1} {RNG.choice(v_pool)} {n2}"
        elif a_pool and n_pool and v_pool:
            phrase = f"{RNG.choice(a_pool)} {RNG.choice(n_pool)} {RNG.choice(v_pool)}"
        else:
            break

        if not phrase:
            continue
        k = phrase.casefold()
        if k in used or not phrase_ok(phrase, counts):
            continue
        used.add(k)
        commit(phrase, counts)
        picked.append(phrase)

    return picked


def take_from_list(
    candidates: list[str],
    need: int,
    counts: Counter[str],
    used: set[str],
    max_occ: int = MAX_OCC,
) -> list[str]:
    picked: list[str] = []
    for phrase in candidates:
        if len(picked) >= need:
            break
        k = phrase.casefold()
        if k in used or not phrase_ok(phrase, counts, max_occ):
            continue
        used.add(k)
        commit(phrase, counts)
        picked.append(phrase)
    return picked


