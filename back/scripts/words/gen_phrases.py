def load_lexicon_slices() -> dict[int, tuple[list[str], list[str]]]:
    """Разбивает adj/noun из банков на 5 непересекающихся срезов."""
    lex_path = HERE / "_phrase_lexicon.py"
    if not lex_path.is_file():
        return {}
    ns: dict = {}
    exec(lex_path.read_text(encoding="utf-8"), ns)
    adj = [w for w in ns.get("ADJ", []) if len(w) >= 4]
    noun = [w for w in ns.get("NOUN", []) if 3 <= len(w) <= 14]
    RNG.shuffle(adj)
    RNG.shuffle(noun)
    out: dict[int, tuple[list[str], list[str]]] = {}
    for lvl in range(1, 6):
        # больше существительных на уровень — запас для лимита <=2
        a = adj[(lvl - 1) * 150 : lvl * 150]
        n = noun[(lvl - 1) * 400 : lvl * 400]
        out[lvl] = (a, n)
    return out


