def parse_curated(raw: list[str]) -> list[str]:
    out = []
    for x in raw:
        p = re.sub(r"\s+", " ", x.strip().strip(","))
        if len(p.split()) >= 3:
            out.append(p)
    return out


def content_tokens(phrase: str) -> list[str]:
    return [t.casefold() for t in phrase.split() if t.casefold() not in STOP]


def phrase_ok(phrase: str, counts: Counter[str], max_occ: int) -> bool:
    parts = phrase.split()
    if not (3 <= len(parts) <= 4):
        return False
    if any(re.search(r"[A-Za-z]", p) for p in parts):
        return False
    for t in content_tokens(phrase):
        if counts[t] >= max_occ:
            return False
    return True


def register(phrase: str, counts: Counter[str]) -> None:
    for t in content_tokens(phrase):
        counts[t] += 1


