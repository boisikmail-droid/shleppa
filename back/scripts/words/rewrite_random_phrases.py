def fill_level(lvl: int, rnd: random.Random) -> list[str]:
    counts: Counter[str] = Counter()
    out: list[str] = []
    seen: set[str] = set()

    for p in parse_curated(CURATED.get(lvl, [])):
        if phrase_ok(p, counts, MAX_OCC[lvl]) and p.casefold() not in seen:
            out.append(p)
            seen.add(p.casefold())
            register(p, counts)

    pools = LEVEL_POOLS[lvl]
    attempts = 0
    target = QUOTAS[lvl]
    while len(out) < target and attempts < target * 120:
        attempts += 1
        pool = rnd.choice(pools)
        with_adv = rnd.random() < 0.75
        phrase = gen_from_pool(pool, rnd, with_adv)
        key = phrase.casefold()
        if key in seen:
            continue
        if not phrase_ok(phrase, counts, MAX_OCC[lvl]):
            continue
        out.append(phrase)
        seen.add(key)
        register(phrase, counts)

    return out


