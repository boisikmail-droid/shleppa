def gen_from_pool(pool: dict, rnd: random.Random, with_adv: bool) -> str:
    adj = rnd.choice(pool["adj"])
    noun = rnd.choice(pool["noun"])
    verbs = pool["verb"]
    if noun.casefold() not in ANIMATE:
        verbs = [v for v in verbs if v.casefold() in INANIMATE_VERBS] or verbs
    verb = rnd.choice(verbs)
    if with_adv and pool.get("adv"):
        adv = rnd.choice(pool["adv"])
        return f"{adj} {noun} {verb} {adv}"
    return f"{adj} {noun} {verb}"


