function buildAllNames(limit = 900) {
  const out = [...READY_NAMES]
  const seen = new Set(out.map((n) => n.toLowerCase()))

  for (const adj of ADJECTIVES) {
    for (const noun of NOUNS) {
      if (out.length >= limit) return out
      const name = `${adj} ${noun}`
      const key = name.toLowerCase()
      if (seen.has(key)) continue
      seen.add(key)
      out.push(name)
    }
  }
  return out
}

export const TEAM_NAME_POOL = buildAllNames(900)

function shuffleInPlace(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[arr[i], arr[j]] = [arr[j], arr[i]]
  }
  return arr
}

/**
 * @param {number} count
 * @param {string[]} [exclude]
 * @returns {string[]}
 */
export function pickTeamNames(count, exclude = []) {
  const blocked = new Set(exclude.map((n) => n.trim().toLowerCase()).filter(Boolean))
  const pool = shuffleInPlace(
    TEAM_NAME_POOL.filter((n) => !blocked.has(n.toLowerCase())).slice()
  )
  const result = []
  for (const name of pool) {
    if (result.length >= count) break
    result.push(name)
  }
  while (result.length < count) {
    result.push(`Команда ${result.length + 1}`)
  }
  return result
}

export function pickOneTeamName(exclude = []) {
  return pickTeamNames(1, exclude)[0]
}
